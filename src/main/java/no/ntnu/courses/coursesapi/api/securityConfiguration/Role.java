package no.ntnu.courses.coursesapi.api.securityConfiguration;

/**
 * Enum representing different roles in the application.
 */
public enum Role {

    /**
     * The ADMIN role.
     * This role grants administrative privileges.
     */
    ADMIN,

    /**
     * The USER role.
     * This role represents a regular user.
     */
    USER,

    /**
     * The MODERATOR role.
     * This role grants moderation privileges.
     */
    MODERATOR
}

